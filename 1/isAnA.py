#Deskripsi: Menerima sebuah karakter dan bernilai benar jika karakter tersebut adalah huruf 'A'

#DEFINISI DAN SPESIFIKASI
#IsAnA: character --> boolean
#{IsAnA(C) benar jika c adalah karakter (huruf) 'A'}

#REALISASI
def isAnA(c):
    return c == "A"

#APLIKASI
print(isAnA(1))
print(isAnA(0))
print(isAnA(-1))


